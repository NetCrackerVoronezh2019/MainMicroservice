package Models;

public class UserAdvertisementInfo {
	
	private String role;
	private Boolean isMyAdvertisement;
	private Boolean sendINotification;
	
	public String getRole() {
		return role;
	}
	public void setRole(String role) {
		this.role = role;
	}
	public Boolean getIsMyAdvertisement() {
		return isMyAdvertisement;
	}
	public void setIsMyAdvertisement(Boolean isMyAdvertisement) {
		this.isMyAdvertisement = isMyAdvertisement;
	}
	public Boolean getSendINotification() {
		return sendINotification;
	}
	public void setSendINotification(Boolean sendINotification) {
		this.sendINotification = sendINotification;
	}
	
	
}
